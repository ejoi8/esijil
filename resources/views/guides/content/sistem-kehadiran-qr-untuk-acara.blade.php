<p>Merekod kehadiran dengan kod QR menggantikan senarai kehadiran kertas, mempercepatkan daftar masuk, dan memberi anda data kehadiran yang tepat untuk tujuan sijil dan pelaporan.</p>

<h2 style="font-size:18px;letter-spacing:-.02em;margin:22px 0 6px">1. Setiap peserta mempunyai kod QR</h2>
<p>Selepas mendaftar, peserta menerima kod QR peribadi. Kod ini dipaparkan pada pas kehadiran mereka dan boleh diimbas di pintu.</p>

<h2 style="font-size:18px;letter-spacing:-.02em;margin:22px 0 6px">2. Imbas untuk daftar masuk</h2>
<p>Petugas membuka halaman pengimbas pada telefon dan mengimbas kod QR peserta. Daftar masuk direkodkan serta-merta tanpa peralatan khas.</p>

<h2 style="font-size:18px;letter-spacing:-.02em;margin:22px 0 6px">3. Mod semak atau mod laju</h2>
<p>Gunakan <em>mod semak</em> untuk melihat dan mengesahkan data peserta sebelum merekod kehadiran, atau <em>mod laju</em> untuk daftar masuk segera di pintu yang sibuk. PIN stesen menghalang capaian tanpa kebenaran.</p>

