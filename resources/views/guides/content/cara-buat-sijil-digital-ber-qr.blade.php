<p>Sijil digital memudahkan peserta menyimpan dan mengesahkan penyertaan mereka tanpa salinan cetak, sambil melindungi penganjur daripada sijil palsu. Berikut panduan ringkas menyediakannya untuk program anda.</p>

<h2 style="font-size:18px;letter-spacing:-.02em;margin:22px 0 6px">1. Sediakan templat sijil</h2>
<p>Tetapkan reka letak sijil — nama penerima, nama program, tarikh dan nombor sijil unik. Setiap sijil yang dijana membawa nombor unik untuk tujuan pengesahan.</p>

<h2 style="font-size:18px;letter-spacing:-.02em;margin:22px 0 6px">2. Kaitkan sijil dengan acara</h2>
<p>Apabila peserta mendaftar dan hadir, sijil boleh dikeluarkan secara automatik atau selepas kehadiran mereka disahkan oleh penganjur — mengelakkan sijil dikeluarkan kepada peserta yang tidak hadir.</p>

<h2 style="font-size:18px;letter-spacing:-.02em;margin:22px 0 6px">3. Benarkan semakan dalam talian</h2>
<p>Peserta menyemak dan memuat turun sijil melalui <a href="{{ route('certificate-lookup.index') }}">halaman semakan sijil</a> menggunakan emel sahaja — tiada akaun diperlukan. Nombor sijil unik membolehkan pihak ketiga mengesahkan kesahihannya.</p>

